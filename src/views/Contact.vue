            street no 14 Sananwetan, Klampok South Blitar, East java</span
          >
        </div>
        <div class="py-4 subheading font-weight-bold">
          <v-icon large color="green" left>fas fa-envelope</v-icon>
          <span>ctrmirna@</span>
          <span class="green--text">gmail.com</span>
        </div>
        <div class="py-4 subheading font-weight-bold">
